# Variable Types

# Integer
x = 1

# Float
y = 1.0

# Complex
z = 1j

print(type(x))
print(type(y))
print(type(z))

# Convert From Int To Float
a = float(x)
# Convert From Float To Int
b = int(y)
# Convert To Complex
c = complex(x)

print(a)
print(b)
print(c)

# String :- Use Single Quote(') Or Double Quote(")
k = "John Smith"

# Multiple Variable Declare with Same Value
ca, us = "+1"
ab = bc = "+1"

# Multiple Variable Declare with Different Values
ind, gb = "+91", "+44"
