a = 10
b = 20

# Mathematical Operators
'''
    Addition :- +
    Subtraction :- -
    Multiplication :- *
    Division :- /
    Modulus :- %
    Exponential :- **
    Floor Division :- //
'''

# Addition
x = a + b
print(x)

# Subtraction
y = a - b
print(y)

# Multiplication
z = a * b
print(z)

# Division
s = a / b
print(s)

# Modulus
d = a % b
print(d)

# Exponential
p = a ** b
print(p)

# Division Answer Without Decimal (Floor Division)
m = b // a
print(m)
